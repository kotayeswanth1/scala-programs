object datatypesExample{
def main(args:Array[String])={

var a=args(0)
println(a)

var b=args(1)
println(b)

var c=args(2)
println(c)


var i:Short=32755
println(i)

var j:Int=2147483645
println(j)

var k:Long=922337203685477500l
println(k)

var l:Float=32.275525555022551f
println(l)

var m:Double=32.5555555555555555555555555555555555555555555555
println(m)

}
}